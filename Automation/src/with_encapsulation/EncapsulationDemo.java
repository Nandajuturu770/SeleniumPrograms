package with_encapsulation;

public class EncapsulationDemo {
	// declaration
	private int i ; 
	// to constructor
	public EncapsulationDemo(int j ) {
		// to initiliazation
		i = j ;
	}
	// to get the value
	public  int  getValue() {
		return i;
	}
	// to set the value
	public void setValue(int j ) {
		i = j ;
	}
}
